package Analisis;

/**
 *
 * @author Jenny
 */
public class Valor {
    int tipo;
    String valor;

    public Valor(int tipo, String valor) {
        this.tipo = tipo;
        this.valor = valor;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }
    
}
